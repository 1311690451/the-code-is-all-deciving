%-------------传递函数转零极点函数----------------%
t = 0 : 0.01 : 12;
num = 5*[1 ,5 ,6];den = [1 ,6 ,10 ,8];          %原三阶建模
[z ,p ,k] = tf2zp(num ,den);                    %转化函数
sys = zpk(z ,p ,k)                             %零极点函数
%-------------传递函数转零极点函数----------------%

%-------------    pzmap     --------------------%
figure(1);
pzmap(sys);
%-------------    pzmap     --------------------%

%-------------    阶跃响应     ------------------%
%--------------p -4 to -0.5-----------%
[p1] = [p];                  %换位置储存
p1(1) = -0.5;k1 = 0.625;      %更改p的参数
sys2 = zpk(z ,p1 ,k1)        %生成零极点模型   
%--------------p -4 to -0.5-----------%

%--------------z -2 to -1  -----------%
[z2] = [z];          %换位置储存
z2(2) = -1;k2 = 10;         %更改z,k的参数
sys3 = zpk(z1 ,p ,k2)       %生成零极点模型 
%--------------z -2 to -1  -----------%

figure(2);
step(sys ,'b-' ,...
    sys2 ,'r:' ,...
    sys3 ,'g-.' ,t);
legend('y' ,'p=-0.5' ,'z=-1');
gtext('y');
gtext('y1,p=-0.5');
gtext('y2,z=-1');
